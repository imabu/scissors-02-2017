package game.models;


public class ClientSnap {
    private int x;
    private int y;


    public ClientSnap(){}

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
